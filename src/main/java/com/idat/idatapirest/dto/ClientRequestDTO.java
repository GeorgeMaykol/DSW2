package com.idat.idatapirest.dto;

public class ClientRequestDTO {
	
	private Integer idRequest;
	private String clienteRequest;
	private String celularRequest;
	
	public Integer getIdRequest() {
		return idRequest;
	}
	public void setIdRequest(Integer idRequest) {
		this.idRequest = idRequest;
	}
	public String getClienteRequest() {
		return clienteRequest;
	}
	public void setClienteRequest(String clienteRequest) {
		this.clienteRequest = clienteRequest;
	}
	public String getCelularRequest() {
		return celularRequest;
	}
	public void setCelularRequest(String celularRequest) {
		this.celularRequest = celularRequest;
	}
	
	

}
